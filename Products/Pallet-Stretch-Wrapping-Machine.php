    function renderProducts(filter = 'all') {
      const container = document.getElementById('products-container');
      container.innerHTML = '';

      const filtered = filter === 'all' 
        ? productsData 
        : productsData.filter(p => p.category === filter);

      filtered.forEach((product, index) => {
        const card = document.createElement('div');
        card.className = 'product-card';
        card.innerHTML = `
          <div class="product-badge">${product.badge}</div>
            <div class="product-model">${product.model}</div>
            <div class="product-name">${product.name}</div>
          </div>
          <div class="product-body">
            <span class="product-type-tag">${product.type}</span>
            <p class="product-description">${product.description}</p>
            <div class="product-features">
              <div class="features-title">Key Features:</div>
              <ul class="feature-list">
                ${product.features.slice(0, 3).map(f => `<li>${f}</li>`).join('')}
              </ul>
            </div>
            <div class="applications-section">
              <div class="applications-title">Applications</div>
              <div class="applications-text">${product.applications}</div>
            </div>
          </div>
          <div class="product-footer">
            <a href="../contact.php" class="btn-learn-more">Request Info</a>
            <a href="../contact.php" class="btn-inquire">Inquire Now</a>
          </div>
        `;
        container.appendChild(card);
      });
    }

